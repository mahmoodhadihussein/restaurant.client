    private void btnAddtoCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddtoCartActionPerformed
        // TODO add your handling code here:

        if(txtItemname.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No item selected.", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            try {
                int qty = Integer.parseInt(txtqty.getText());

                txttotal.setText(Integer.toString(qty * Integer.parseInt(txtprice.getText())));

                ((DefaultTableModel)jtCart.getModel()).addRow(new Object[]
                                                                {txtItemname.getText(), 
                                                                 txtprice.getText(), 
                                                                 txtqty.getText()});
                
                if(txtGrandTotal.getText().equals("")) {
                   txtGrandTotal.setText(txttotal.getText());
                }
                else {
                    txtGrandTotal.setText(Integer.toString(Integer.parseInt(txtGrandTotal.getText()) + Integer.parseInt(txttotal.getText())));
                }

                txtItemname.setText("");
                txtprice.setText("");
                txtqty.setText("");
                txttotal.setText("");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Invalid quantity.", "Error", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddtoCartActionPerformed

    private void btnPlaceTheOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceTheOrderActionPerformed
        if(jtCart.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "No Order to Place.", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            try {
                // TODO add your handling code here:
                //Socket s = new Socket("localhost",8080);  //Test on the machine
                Socket s = new Socket("192.168.10.5",8080);
                DataInputStream din = new DataInputStream(s.getInputStream());  
                DataOutputStream dout = new DataOutputStream(s.getOutputStream());    

                // Collect data
                String data = "";
                for(int i = 0; i < jtCart.getRowCount(); i++) {
                    data += jtCart.getValueAt(i, 0) + "#" +
                            jtCart.getValueAt(i, 1) + "#" +
                            jtCart.getValueAt(i, 2) + "#";

                    if(i < jtCart.getRowCount() - 1) {
                        data += ",";
                    }
                }

                dout.writeUTF(data);
                dout.flush();

                String result = din.readUTF();  
                
                System.out.println("Server Message:" + result);
                
                if(result.equals("OK")) {
                    JOptionPane.showMessageDialog(null, "Order Saved Successfully.", "Save", JOptionPane.INFORMATION_MESSAGE);
                }
                else {
                    JOptionPane.showMessageDialog(null, "Cannot save order.", "Error", JOptionPane.INFORMATION_MESSAGE);
                }

                ((DefaultTableModel)jtCart.getModel()).setRowCount(0);

                txtGrandTotal.setText("");

                dout.close();
                din.close();
            } catch (IOException ex) {
                Logger.getLogger(Order.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnPlaceTheOrderActionPerformed

