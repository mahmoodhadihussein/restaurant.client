    private void lstItemsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstItemsValueChanged
        // TODO add your handling code here:
        if(lstItems.getSelectedIndex() > -1) {
            String item = lstItems.getModel().getElementAt(lstItems.getSelectedIndex()).toString(); 
            
            txtItemname.setText(item.split(" # ")[0]);
            txtqty.setText("1");
            txtprice.setText(item.split(" # ")[1]);
            txttotal.setText(item.split(" # ")[1]);
        }
        
    }//GEN-LAST:event_lstItemsValueChanged

